/**
 * Adds a number to number.
 * @param a - First number.
 * @param b - Second number.
 * @returns Number.
 */
function add(a: number, b: number): number {
  return a + b;
}
/**
 * Subtracts a numbers from number.
 * @param a - First number.
 * @param b - Second number.
 * @returns Number.
 */
function subtract(a: number, b: number): number {
  return a - b;
}
export { add, subtract };
